package model;

public class Edge {

	private int nodeFrom;
	private int nodeTo;
	
	
	public Edge(int nodeFrom, int nodeTo) {
		super();
		this.nodeFrom = nodeFrom;
		this.nodeTo = nodeTo;
	}


	public int getNodeFrom() {
		return nodeFrom;
	}


	public int getNodeTo() {
		return nodeTo;
	}
	
	
	
	
	
	
}
